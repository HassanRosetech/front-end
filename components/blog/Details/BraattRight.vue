            <h2 class="card-title">Bra att tänka på vid köp av scooter!</h2>
            <p class="font-light first-latter">

              Drömmer du om att ta dig från ena sidan av staden till den andra på mindre än en timme? Eller att susa fram längs vägen till jobbet istället för att sitta i bilden eller på tåget? Ja med en elscooter är det möjligt att göra det och det finns en uppsjö av olika modeller på marknaden. Elscootrar kallas också för elektrisk sparkcykel, elsparkcykel och elkickbike, för att nämna några av de vanligaste benämningarna. De flesta modellerna är hopfällbara och kan tas med på bussen eller tåget, vilket är smidigt för den som pendlar.
<h2 id="kopguide">Köpguide</h2>
En elscooter är perfekt för dig som vill transportera dig snabbt, enkelt och miljövänligt. Om du väljer en el scooter som fått <em>bäst i test</em> har du en viss garanti att de har kontrollerats. Det finns dock en hel del att tänka på innan du köper en elscooter och väljer ut den <em>bästa elscootern</em> för dig!
<h3 id="ramen">Ramen</h3>
I de flesta <em>elscooter test</em> finns det uppgifter om ramen. Kontrollera att den är gjord av ett hållbart material som klarar det du ska använda den till. Om du ska köra på släta vägar kan du förmodligen nöja dig med en lättare modell men om du behöver köra på mindre släta vägar eller kanske till och med på skogstigar är det viktigt att välja en mer robust elscooter.
<h3 id="motorn">Motorn</h3>
Motorn på en elscooter sitter inne i hjulet och batteriet oftast i brädan som du står på. Kontrollera att båda dessa är väl skyddade av vattentäta material. Som bekant är vatten och el ingen bra kombination. Var noga med att kontrollera att din elscooter är vattentät och kör aldrig om det regnar eller genom vattenpölar. Speciellt inte om brädan hamnar under vatten.
<h3 id="hjul_och_stotdampare">Hjul och stötdämpare</h3>
En del elscootrar har hårda små hjul och en del har större luftfyllda hjul. En del har inga stötdämpare, vissa har enklare stötdämpning och några få modeller har rejäl stötdämpning. Alla dessa faktorer påverkar hur det känns att åka med elscootern och hur bekväm den är. Luftfyllda eller större däck och dubbla stötdämpare fram och bak ger bättre komfort.
<h3 id="bromsar">Bromsar</h3>
Se till att din elscooter har bromsar både fram och bak. Det bästa är om den både har en elektrisk broms och en mekanisk. Testbromsa med elscootern när du provkör den för att säkerställa att den bromsar som den ska. Tänk också på att din vikt påverkar bromssträckan.
<h3 id="led_belysning">LED-belysning</h3>
Se till att scootern har LED-lampor så du kan köra i mörkret på ett säkert sätt.
<h3 id="hjalm">Hjälm</h3>
Rent juridiskt råder hjälmkrav för de under 15 år i Sverige men även du som vuxen behöver skydda huvudet, oavsett om du åker elscooter, cykel eller kör moped. Investera i en bra hjälm som är godkänd och testad. Ett tips är att titta på hjälmar för mopedåkare eftersom dessa är gjorda för att skydda huvudet på ett annat sätt än till exempel cykel- eller ridhjälmar.
<h3 id="laddning_vs_stracka">Laddning vs sträcka</h3>
Hur långt du kan ta dig på din elscooter innan du behöver ladda upp den igen är också viktigt att tänka på. Det finns elscootrar med 12 kilometers räckvidd på en laddning men också sådana som har upp till 50 kilometer på en laddning. Skillnaderna är alltså ganska stora. Det kan därför vara smart att tänka igenom hur mycket och när du kommer använda din elscooter. Om du ska åka till jobbet med den är det ju inte kul om den lägger av efter halva vägen… Utetemperaturen påverkar också räckvidden eftersom batterier tappar i kapacitet vid lägre temperaturer. Utöver det är det bra att tänka på att batteriets kapacitet, generellt sett, minskar med ca 10-20% per år.
<h3 id="watt">Watt</h3>
Antalet watt anger hur stark elmotorn är i din elscooter och avgör också hur långt du kan köra och på vilken slags underlag. En 250 watt elscooter är oftast anpassade för barn medan en elscooter med 350 watt passar en ungdom eller mindre vuxen. För en vuxen person krävs alltså högre antal watt. Vanligen börjar vuxen elscootrar kring 350 watt och uppåt där 350 watt motsvarar en person på ca 50-55 kilo i vikt. Ju mer du väger, desto högra watt är att rekommendera.
<h3 id="vikt">Vikt</h3>
Visserligen kommer du åka på den men du kommer också behöva bära den ibland. Kanske ner för trapporna i ditt hem eller upp för trapporna på jobbet. När den laddat ur behöver du också bära den. Det är därför vikten spelar större roll än man kanske tänker på.
<h3 id="max_last">Max last</h3>
De flesta elscooters har en max last angiven. Respektera denna och var noga med att inte överlasta din scooter. Det kan resultera i att till exempel bromsar inte fungerar som de ska och du utsätter dig för onödiga risker eller kan inte väja för den där trafikanten som inte såg dig komma.
<h3>Rengöring av elscootern</h3>
Rengör aldrig ett eldrivet fordon i högtryckstvätt eller en kraftig vattenstråle. Vi rekommenderar sparsamt med vatten och en trasa. Undvik alkohol och ammoniak i rengöringsmedel, då dessa löser upp de vattentäta listerna.
<h3>Service och garantier</h3>
Din elscooter kräver underhåll. Någonting kan gå sönder, du kanske behöver nya batterier eller byta ut dina slitna bromsbelägg. Du kanske behöver reservdelar eller andra slags reparationer. Oavsett vad som hänt med din elscooter så måste problemet åtgärdas innan du ger dig ut på vägarna igen.
<h3 id="vad_lagen_sager">Vad lagen säger</h3>
Du får inte köra elskotern på trottoaren utan bara köra på cykelväg eller ute i vägbanan. För eldrivna fordon är maximal tillåten toppfart 20 km/h. Om du har B-körkort får du köra upp till 45 km/h. Tänk på att du är förare av ett fordon när du kör elscooter. Det gäller att tänka sig för när man kör med sin elscooter så att man inte kör för fort eller riskerar att skada sig själv eller någon annan i trafiken.
<h2 id="slutligen">Slutligen</h2>
Det är kul, miljövänligt och härligt med en elscooter men det är också viktigt att hålla koll på både trafikregler och utrustning innan man ger sig iväg och införskaffar en. Kontrollera däck, broms, ramen och vikten. Kolla också laddningstider och hur långt du kan köra. Glöm heller inte hjälmen! Ha så kul med din nya elscooter!

