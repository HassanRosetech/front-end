I detta avsnitt ska vi titta närmare på problemet felkod 15 och hur man kan lösa felet för att fortsätta åka på sin elscooter utan avbrott!
<h5>ANLEDNING OCH KONTROLL PUNKTER</h5>
<strong>Felkod 15</strong> är den mest vanligast förekommande handhavande fel som alla Xiaomi Electric scooter förare råkar ut för. <strong>Felaktig Handbroms! </strong>

I handbromsen sitter det två magnetpoler som reagerar på respons för E-broms och mekanisk inbromsning som är ömtålig. Oftast räcker det med att scootern faller ner på backen så glider polerna isär eller om scootern utsatts för regn och vatten som orsakat korrosion. Detta kommer orsaka med felkod 15.

<strong>Var sitter magnet polerna ? </strong>

<strong><img src="https://cdn.shopify.com/s/files/1/0572/4009/2821/files/Handbroms_magnetpoler_480x480.jpg?v=1642334182" alt="" /></strong>

<em>Som ni ser på min röda bildmarkering sitter det två skruvar som skyddar magnetpolerna och komponenterna. Obs! Byt ut hela bromshandtaget . Denna handbroms är standard för alla Xiaomi scooter modeller och till samtliga Segways från serien G30. </em>

&nbsp;

Det är inte värt att reparera eller ersätta magnetpolerna i detta fall då självaste resevdelen är low-cost (billig reservdel).
<br>

Steg 1 : Börja med att avlägsna gummihandtaget, <em>(ett tips : Använd två händer och försök att komma in med fingrarna från sidan och dra / pusha mot med fingrarna ut)</em>. Använd insexnyckel för att avlägsna skruven.
<br>
Steg 2 : Nu är det dags att demontera displayen för att avlägsna handbromskontakten.
<br>
1. Använd hårfön eller värmepistol (va försiktig) mot displayen och försiktigt pilla bort den tvådelade skyddsdisplayen.
<br>
2. Skruva bort dem tre skruvarna som håller displayen på plats.
<br>
3. Klipp bort buntbanden från den gråa Silicon kapseln och koppla bort kontakterna.
<br>
4. Det sitter två skruvar ( upp/ned) som håller belysningen, skruva bort det.
<br>
5. Avlägsna bromsvajern från gashandtaget <em>(Obs! Med tiden så förekommer det rost i bromsvajer och kan bli skör, va därför försiktig när du tar i. Tänk på att det kan gå av )</em>
<br>
<em>I samband med byte av handbroms kan ni bli tvungen att justera bromsvajern via bromsoket . Ställ in rätt hårdhet i inbromsning. </em>

Nu är det bara att avlägsna bromshandtaget och ersätta med en ny. Plocka ihop allt och starta igång scootern.

