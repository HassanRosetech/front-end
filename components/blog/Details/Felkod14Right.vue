I denna avsnitt ska vi titta närmare på problemet felkod 14 och hur man kan lösa felet för att fortsätta åka på sin elscooter.
<br>
<h5>ANLEDNING OCH KONTROLL PUNKTER</h5>
<br>
Felkod 14 är den mest vanligast förekommande handhavande fel som alla Xiaomi Electric scooter förare råkar ut för. <strong>Felaktig gasreglage</strong>!

I gasreglaget sitter det två magnetpoler som reagerar på respons och är ömtålig. Oftast räcker det med att scootern faller ner på backen så glider polerna isär. Detta kommer orsaka med felkod 14 eller om distansen mellan gummihandtaget och gasreglaget är tajt att den tar emot så kommer du också få felkod 14.
<br>
Steg 1 : Börja med att kontrollera så att gummihandtaget inte tar emot gasreglaget, prova dra ut gummihandtaget några millimeter och starta om din scooter.

<strong>Kvarstår problemet ?</strong> Då är det dags att byta ut gasreglaget ! (Köp gasreglaget <a href="https://www.partshop.se/products/xiaomi-scooter-gasreglage?_pos=1&amp;_sid=bf3202111&amp;_ss=r">här</a>) Detta gör du genom att avlägsna styret från scootern genom att skruva bort fyra skruvar som håller ihop styret .
<br>
<br>
1. Använd hårfön eller värmepistol (va försiktig) mot displayen och försiktigt pilla bort den tvådelade skyddsdisplayen.
<br>
<br>
3. Skruva bort dem tre skruvarna som håller displayen på plats.
<br>
<br>
4. Klipp bort buntbanden som från den gråa silicone kapsel och koppla bort kontakterna.
<br>
<br>
5. Det sitter två skruvar ( upp/ned) som håller belysningen, skruva bort det.
<br>
<br>
Nu är det mesta förarbete klar och du behöver avlägsna gummihandtaget och skruva bort gasreglaget. Ersätt med en ny reglage och plocka ihop allt.

